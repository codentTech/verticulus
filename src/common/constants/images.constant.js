// Helper function to get image with specific dimensions
export const getImage = (imageKey, width = 400, height = 300) => {
  const baseUrl = IMAGES[imageKey];
  if (!baseUrl) return IMAGES.PLACEHOLDER_SQUARE;

  // If it's already an Unsplash URL, modify dimensions
  if (baseUrl.includes("unsplash.com")) {
    return baseUrl.replace(/w=\d+&h=\d+/, `w=${width}&h=${height}`);
  }

  return baseUrl;
};

// Helper function to get responsive image
export const getResponsiveImage = (
  imageKey,
  sizes = { sm: 400, md: 600, lg: 800 }
) => {
  const baseUrl = IMAGES[imageKey];
  if (!baseUrl) return IMAGES.PLACEHOLDER_SQUARE;

  return {
    sm: getImage(imageKey, sizes.sm, Math.round(sizes.sm * 0.75)),
    md: getImage(imageKey, sizes.md, Math.round(sizes.md * 0.75)),
    lg: getImage(imageKey, sizes.lg, Math.round(sizes.lg * 0.75)),
  };
};
